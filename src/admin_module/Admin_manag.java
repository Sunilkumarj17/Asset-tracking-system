    private void addAdminButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAdminButtonActionPerformed
        // TODO add your handling code here:
        int flag = 0;
         if(username.getText().trim().isEmpty() && user_id.getText().trim().isEmpty() && password1.getText().trim().isEmpty() && email.getText().trim().isEmpty() && designation.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "All fields are empty");
            flag += 1;
            }
            else if(username.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Username field is empty");flag +=1;
            }
            else if(user_id.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Userid field is empty");flag+=1;
            }
            else if(password1.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Password field is empty");flag+=1;
            }
            else if(email.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Email field is empty");flag+=1;
            }
            else if(designation.getText().trim().isEmpty())
            {
                JOptionPane.showMessageDialog(null, "Designation field is empty");flag+=1;
            }
            else {
                 JOptionPane.showMessageDialog(null, "Details are added");
                   
        try {
            String sql = "Insert into admin (user_id,username,password,email,designation,type) values(?,?,?,?,?,?)";
            PreparedStatement stm = connection.prepareStatement(sql);
            stm.setString(1, user_id.getText());
            stm.setString(2, username.getText());
            stm.setString(3, password1.getText());
            stm.setString(4, email.getText());
            stm.setString(5, designation.getText());
            stm.setString(6, "admin");
            stm.execute();
   
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex);
            Logger.getLogger(Admin_manag.class.getName()).log(Level.SEVERE, null, ex);       
        }
        username.setText("");
        user_id.setText("");
        password1.setText("");
        email.setText("");
        designation.setText("");
        this.dispose();
        Admin_manag a = new Admin_manag();
        Vp.setVisible(true);
        Ap.setVisible(false);
        Up.setVisible(false);
        a.setVisible(true);
            }
        
         
    }//GEN-LAST:event_addAdminButtonActionPerformed

        
