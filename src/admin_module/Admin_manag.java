    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        int i = ATable.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel)ATable.getModel();
        if (i>=0) {
            model.setValueAt(username1.getText(), i, 0);
            model.setValueAt(user_id1.getText(), i, 1);
            model.setValueAt(password2.getText(), i, 2);
            model.setValueAt(email1.getText(), i, 3);
            model.setValueAt(designation1.getText(), i, 4);
            
            try{
            PreparedStatement pst1 = connection.prepareStatement("UPDATE admin set username=?,password=?,email=?,designation=? where user_id=?;");
            pst1.setString(1,username1.getText());
            pst1.setString(2,password2.getText());
            pst1.setString(3,email1.getText());
            pst1.setString(4,designation1.getText());
            pst1.setString(5,user_id1.getText());
            pst1.executeUpdate();
            JOptionPane.showMessageDialog(null,"Details updated");
       
            username1.setText("");
            user_id1.setText("");
            password2.setText("");
            email1.setText("");
            designation1.setText("");
            this.dispose();
        Admin_manag a = new Admin_manag();
        Vp.setVisible(true);
        Ap.setVisible(false);
        Up.setVisible(false);
        a.setVisible(true);
            }
            catch(Exception e){System.out.println(e);}
          
        }
        else {
            JOptionPane.showMessageDialog(null,"Error");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

