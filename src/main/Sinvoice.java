        ResultSet rs1 = pst.executeQuery();
                byte[] imge = new byte[0];

        if(rs1.next())
        {
            imge = rs1.getBytes("invoice_image");
        }

            byte[] encodedBytes = Base64.getEncoder().encode(imge);
