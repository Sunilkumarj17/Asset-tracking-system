                   String encodedURL = Base64.getUrlEncoder().encodeToString(encodedString.getBytes());
                   byte[] decodedBytes = Base64.getDecoder().decode(encodedString.getBytes());
                   
                   byte[] imagedata1 = DatatypeConverter.parseBase64Binary(dataURL.substring(dataURL.indexOf(",")+1));
                   Image bufferedImage = ImageIO.read(new ByteArrayInputStream(imagedata1));
                   
                   ImageIcon imageIcon = new ImageIcon(new ImageIcon(bufferedImage).getImage().getScaledInstance(Sinvoice.Slbl_img.getWidth(),Sinvoice.Slbl_img.getHeight(),bufferedImage.SCALE_SMOOTH));
                   Sinvoice.Slbl_img.setIcon(imageIcon);
                   invoice_image1 = decodedBytes;
                                      

